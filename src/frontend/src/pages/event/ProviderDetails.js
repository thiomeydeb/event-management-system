export default function ProviderDetails({ eventData }) {
  const providerArray = Object.keys(eventData.providers).map((key) => {
    const array = { [key]: eventData.providers[key] };
    return array;
  });
  return (
    <TableContainer component={Paper}>
      <Table aria-label="collapsible table">
        <TableHead />
        <TableBody>
          {providerArray.map((row, index) => (
            <Row key={index} row={row} />
          ))}
        </TableBody>
      </Table>
    </TableContainer>
  );
}
